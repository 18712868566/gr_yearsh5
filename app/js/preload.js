let length = mainfest.length;
let images = new Array(); // 定义一个数组容器，用来存储预加载完成的图片

function preloadImg() {
    let count = 0; // 计算器，计算加载了多少图片
    for (let i = 0; i < length; i++) {
        images[i] = new Image();
        images[i].src = `${mainfest[i].src}`;
        // 谷歌浏览器高版本支持大部分ES6，所以这里就不用字符串拼接了。
        // console.log(i)
        images[i].onload = function() {
            count++;
            if (count === length) {
                // console.log('加载完成');
                // shuangjie.$pageLoad.addClass('hide').next().removeClass('hide')
                $('.loading').hide();
                // $('.page1').hide();
                // $('.page1').show();
            } else {
                let precc_widht = Math.round(count / length * 100);

                // console.log(precc_widht)
                $(".progress_wrap .progress_cc .progress").css({
                    width:precc_widht+1+'%'
                });
            }
        }
    }
}

preloadImg();
